# Operator assingment ( = ) 
# fungsinya memberikan nilai pada sebelah kiri operator dengan value nya adalah sebelah kanan operator

# Operator aritmatika biasa : 
# tambah, kurang, kali, bagi
# +  ,  -  ,  *  ,  /

# 1. Modulus => %
# operator modulus digunakan untuk mencari sisa pembagian
# 3 % 2 = 1  => 3 modulus 2 hasilnya adalah 1, 
# jika 3 kita bagi dengan 2, maka akan tersisa bilangan 1
modulus = 5 % 2
print(modulus)  # 1


# 2. Pangkat => **
# operator pangkat menggunakan tanda * sebanyak 2 kali,
# digunakan untuk memangkatkan bilangan didepan tanda ** sebanyak bil dibelakang tanda **
# 2**3 = 8 => 2 dipangkatkan 3 sama dengan 8
pangkat = 5**4
print(pangkat)  # 625